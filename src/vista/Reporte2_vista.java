    public void mostrar(){
        txt_nombre_equipo.setText(lista.get(contador).getNombre_equipo());
        txtnumero_equipo.setText(Integer.toString(lista.get(contador).getNumero_equipo()));
        txt_nacionalidad_equipo.setText(lista.get(contador).getNacionalidad_equipo());
        
        txt_posicion.setText(Integer.toString(lista.get(contador).getPosicion_actual()));
        txt_km.setText(Long.toString(lista.get(contador).getKm_recorrido()));
        txt_numero_vuelta.setText(Integer.toString(lista.get(contador).getNumero_vuelta()));
        txt_velocidad_media.setText(Integer.toString(lista.get(contador).getVelocidad_media()));
        
        txt_mejor_vuelta.setText(lista.get(contador).getTiempo_mejor_vuelta().toString());
        
        if(lista.get(contador).getSegundo_nombre() != null)
        txt_nombre_conductor.setText(lista.get(contador).getPrimer_nombre()+" "+lista.get(contador).getSegundo_nombre());
        else
             txt_nombre_conductor.setText(lista.get(contador).getPrimer_nombre());
        
        if(lista.get(contador).getSegundo_apellido() != null)
        txt_apellido.setText(lista.get(contador).getPrimer_apellido()+" "+lista.get(contador).getSegundo_apellido());
        else
        txt_apellido.setText(lista.get(contador).getPrimer_apellido());
       
        txt_nacionalidad_conductor.setText(lista.get(contador).getNacionalidad_conductor());
        
        txt_nombre_automovil.setText(lista.get(contador).getNombre_automovil());
        txt_categoria.setText(lista.get(contador).getNombre_categoria());
        txt_motor.setText(lista.get(contador).getNombre_motor_automovil()+" "+lista.get(contador).getCombustion_motor_automovil()+" "+lista.get(contador).getCilindrada_motor_automovil());
    }
 
    
