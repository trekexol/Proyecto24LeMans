    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
            nohay.setText("");
        btnsiguiente1.setVisible(true);
        btnanterior.setVisible(true);
        
       String ano = combo_ano.getSelectedItem().toString();
        
       
       String categoria = combo_categoria.getSelectedItem().toString();
       
       String var = combo_hora.getSelectedItem().toString();
       
       Time tiempo = Time.valueOf(var);
       
        this.lista = reporte_conexion.reporte2(ano, categoria,tiempo);
        
        mostrar();
        
        }catch(Exception e){
            nohay.setText("No hay Registros de esta Busqueda");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

